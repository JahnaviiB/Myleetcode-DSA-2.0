public class Equality {
    public static void main(String[] args){
        int[] a1 = {2,4,7,11,3};
        int[] a2 = {2,4,7,11,3};
        boolean val;
        for(int i=0;i<a1.length;i++){
            if(a1[i] != a2[i]){
                val = false;
            }
            else{
                val = false;
            }
            if(val){
                System.out.println("Equal");
            }
            else{
                System.out.println("Not Equal");
            }
        }
    }
}
